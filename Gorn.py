def repdict(cs):
    global alphabet
    d={}
    l = cs.split(" ")
    for i in alphabet:
        d[i] = [i]
    for i in l:
        if not i[0] in d:
            d[i[0]] = [i[1]]
        else:
            d[i[0]].append(i[1])
        if not i[1] in d:
            d[i[1]] = [i[0]]
        else:
            d[i[1]].append(i[0])
    return d

